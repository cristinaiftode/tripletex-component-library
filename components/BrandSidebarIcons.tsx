export type BrandSidebarIconProps = {
  /** Pixel size — defaults to 32, the Figma source size. */
  size?: number;
  className?: string;
  "aria-label"?: string;
  /** Override the brand color. Use sparingly — defeats the purpose. */
  color?: string;
};

function makeIcon(name: BrandSidebarIconName) {
  const entry = BRAND_SIDEBAR_ICONS[name];
  const Component: React.FC<BrandSidebarIconProps> = ({
    size = 32,
    className,
    "aria-label": ariaLabel,
    color,
  }) => {
    const classes = ["tt-brand-sidebar-icon", `tt-brand-sidebar-icon--${name}`, className]
      .filter(Boolean)
      .join(" ");
    return (
      <svg
        className={classes}
        width={size}
        height={size}
        viewBox="0 0 32 32"
        fill="none"
        xmlns="http://www.w3.org/2000/svg"
        role={ariaLabel ? "img" : undefined}
        aria-label={ariaLabel}
        aria-hidden={ariaLabel ? undefined : true}
      >
        <path d={entry.path} fill={color ?? entry.color} />
      </svg>
    );
  };
  Component.displayName = `BrandSidebar${name
    .split("-")
    .map((s) => s[0].toUpperCase() + s.slice(1))
    .join("")}Icon`;
  return Component;
}

